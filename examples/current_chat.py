"""获取当前聊天名称示例

此示例演示如何获取并打印当前打开的聊天窗口名称。

作者: CMX

使用场景:
    - 验证当前是否在正确的聊天中
    - 调试时确认聊天切换是否成功
    - 自动化脚本中的状态检查
    - 日志记录当前操作的聊天
    
功能说明:
    - 连接到微信客户端
    - 读取当前聊天对象的名称
    - 打印到控制台
    
运行方式:
    # 先手动打开一个聊天窗口，然后运行：
    uv run python examples/current_chat.py

输出示例:
    张三
    # 或者
    文件传输助手
    # 如果未打开任何聊天，可能输出:
    None

注意事项:
    - 运行前需手动打开一个聊天窗口
    - 如果未打开任何聊天，返回None
    - 对于群组聊天，返回群组名称
    - 名称可能与显示略有不同（去除表情等）
    
扩展用法:
    # 在代码中使用
    client = WeChatClient.auto()
    
    # 获取当前聊天
    chat_name = client.current_chat()
    
    # 检查是否在正确的聊天中
    if chat_name != "目标联系人":
        client.chat_with("目标联系人")
        
    # 用于日志记录
    logger.info(f"当前操作聊天: {chat_name}")
"""

from wechat_sdk import WeChatClient


def main() -> None:
    """主函数：获取并打印当前聊天名称
    
    该函数演示了最简单的聊天状态查询：
    1. 创建微信客户端实例
    2. 调用current_chat()获取当前聊天名称
    3. 打印结果
    
    返回值说明:
        - 字符串：当前聊天的名称（联系人或群组名）
        - None：未打开任何聊天窗口
        
    实际应用:
        - 在执行消息发送前确认目标
        - 监控聊天切换事件
        - 调试UI自动化流程
        - 记录操作上下文
        
    注意:
        - 这是只读操作，不会改变任何状态
        - 可以快速多次调用
        - 适合用作其他操作的前置检查
    """
    # 自动检测并创建微信客户端实例
    client = WeChatClient.auto(mode="auto")
    
    # 获取当前打开的聊天对象名称
    # 如果未打开任何聊天，返回None
    print(client.current_chat())


if __name__ == "__main__":
    main()
